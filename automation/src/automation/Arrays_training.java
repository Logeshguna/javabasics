package automation;

public class Arrays_training {

	public static void main(String[] args) {
		int a[]= {1,6,7,2,5};
		System.out.println("length of a is "+a.length);
        for (int i=0;i<a.length;i++) {
        	System.out.println(a[i]);
        }
        for(int i:a) {
        	System.out.println(i);
        }
        
	}

}
